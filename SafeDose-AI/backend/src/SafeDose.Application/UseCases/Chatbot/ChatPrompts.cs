    public static string DetectIntentFromReply(string reply)
    {
        var trimmed = reply.TrimStart();
        if (trimmed.StartsWith("{") && trimmed.Contains("\"intent\""))
            return "symptom";
        if (trimmed.Contains("شاشة 'فحص التداخلات الدوائية'", StringComparison.OrdinalIgnoreCase)
         || trimmed.Contains("فحص التداخلات", StringComparison.OrdinalIgnoreCase))
            return "interaction_redirect";
        if (trimmed.Contains("مساعدك للأدوية والأعراض", StringComparison.OrdinalIgnoreCase))
            return "out_of_scope";
        if (trimmed.Contains("سجل دخول", StringComparison.OrdinalIgnoreCase))
            return "needs_login";
        return "drug_info";
    }

